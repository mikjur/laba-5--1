упорядочен([]).                 % Пустой список упорядочен
упорядочен([_]).                % Список из одного элемента упорядочен
упорядочен([X,Y|Хвост]) :-      % Для списка из двух и более элементов
    X =< Y,                     % Проверяем что первый <= второму
    упорядочен([Y|Хвост]).      % Рекурсивно проверяем остаток

начало :-                       % Главный предикат
    write('=== Проверка упорядоченности списка ==='), nl,
    write('Введите список в формате [1,2,3]: '),
    read(Список),                % Чтение ввода
    nl,
    write('Проверяем список: '), write(Список), nl,
    (упорядочен(Список) ->       % Проверка условия
        write('Результат: упорядочен по возрастанию'), nl;
        write('Результат: НЕ упорядочен по возрастанию'), nl),
    nl,
    write('Для выхода введите "стоп", для продолжения - любой символ: '),
    read(Ответ),
    (Ответ \== стоп -> начало; true).
