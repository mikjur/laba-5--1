% Факты: подозреваемые
подозреваемый(а).
подозреваемый(b).
подозреваемый(c).
подозреваемый(d).

% Решение задачи
решение :-
    % Возможные нарушения (true - нарушил, false - не нарушил)
    (НарушилА = true ; НарушилА = false),
    (НарушилB = true ; НарушилB = false),
    (НарушилC = true ; НарушилC = false),
    (НарушилD = true ; НарушилD = false),

    % Условие 1: если А нарушил, то и В нарушил
    (НарушилА == true -> НарушилB == true ; true),

    % Условие 2: если В нарушил, то С нарушил или А не нарушил
    (НарушилB == true -> (НарушилC == true ; НарушилА == false) ; true),

    % Условие 3: если D не нарушил, то А нарушил, а С не нарушил
    (НарушилD == false -> (НарушилА == true, НарушилC == false) ; true),

    % Условие 4: если D нарушил, то и А нарушил
    (НарушилD == true -> НарушилА == true ; true),

    % Вывод результатов
    format('Результаты расследования:~n'),
    format('Антипов (~w)~n', [НарушилА]),
    format('Борисов (~w)~n', [НарушилB]),
    format('Цветков (~w)~n', [НарушилC]),
    format('Дмитриев (~w)~n', [НарушилD]),

    % Проверка уникальности решения
    (НарушилА == true -> write('Антипов нарушил правила~n') ; true),
    (НарушилB == true -> write('Борисов нарушил правила~n') ; true),
    (НарушилC == true -> write('Цветков нарушил правила~n') ; true),
    (НарушилD == true -> write('Дмитриев нарушил правила~n') ; true).
